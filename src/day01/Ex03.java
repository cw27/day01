package day01;

public class Ex03 {

	public static void main(String[] args) {
		System.out.println(100);	//일반 숫자는 큰 따옴표 없이 숫자만 입력
		System.out.println(100.123);
		
		System.out.println(100 + 100);	//일반 숫자는 큰 따옴표 없이 숫자만 입력
		System.out.println(100.123 + 100.123);
		
		System.out.println("백 : " + 100);
		System.out.println("100" + 100 + 100);
		System.out.println("100" + (100 + 100));
	}

}
